%8 to 1 mux 
function[out]=mux8(s2,s1,s0,d0,d1,d2,d3,d4,d5,d6,d7)
if s2 <= 1 &&s1 <= 1 && s0 <= 1 && d0 <=1 && d1 <= 1&& d2 <= 1&& d3 <= 1 && d4 <=1 && d5 <= 1&& d6 <= 1&& d7 <= 1
A=notgate(s2);
B=notgate(s1);
C=notgate(s0);
g1=andgate(A,B);
e1=andgate(g1,C);
f1=andgate(e1,d0);
g2=andgate(A,B);
e2=andgate(g2,s0);
f2=andgate(e2,d1);
g3=andgate(A,s1);
e3=andgate(g3,C);
f3=andgate(e3,d2);
g4=andgate(A,s1);
e4=andgate(g4,s0);
f4=andgate(e4,d3);
g5=andgate(s2,B);
e5=andgate(g5,C);
f5=andgate(e5,d4);
g6=andgate(s2,B);
e6=andgate(g6,s0);
f6=andgate(e6,d5);
g7=andgate(s2,s1);
e7=andgate(g7,C);
f7=andgate(e7,d6);
g8=andgate(s2,s1);
e8=andgate(g8,s0);
f8=andgate(e8,d7);
x=orgate(f1,f2);
y=orgate(f3,f4);
T=orgate(f5,f6);
U=orgate(f7,f8);
O1=orgate(x,y);
O2=orgate(T,U);
out=orgate(O1,O2);
else 
    fprintf("entered values are invalid! enter 0 or 1")
end
end